// What’s the difference between undefined, null, and NaN?

// Term | Meaning | Example
// undefined | A variable declared but not assigned a value yet. | let x; console.log(x); // undefined
// null | A value that means "no value" or "empty" (set intentionally). | let y = null;
// NaN | Means "Not a Number" — result of an invalid math operation. | let z = 5 / 'hello'; console.log(z); // NaN

/**
    undefined → default absence of value.
    null → intentional absence of value.
    NaN → error in numeric operations.
 */
